    private void tambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahButtonActionPerformed
        String dataCustomer = "";
        String dataPesanan = "";
        nama = customerNamaValue.getText();
        alamat = customerAlamatValue.getText();
        telepon = customerTeleponValue.getText();
        if (nama.equals("") || alamat.equals("") || telepon.equals("")) {
            dataPenjualanValue.setText("Data Customer Belum Lengkap!");
        } else {
            dataCustomer = ("==================== CHECKOUT RESTORAN ===================="
                    + "\n---------------------------------------------------------------------------------------------------------"
                    + "\nNama           : " + nama
                    + "\nAlamat         : " + alamat
                    + "\nNo Telepon : " + telepon
                    + "\n---------------------------------------------------------------------------------------------------------"
                    + "\n========================== PESANAN =========================\n");
            for (int i = 0; i < pesanan.size(); i++) {
                dataPesanan += "> " + pesanan.get(i) + "\n";
            }
            dataPenjualanValue.setText(dataCustomer + dataPesanan + "\nTotal Pembayaran: Rp" + totalPembelian + ",00");
        }
    }//GEN-LAST:event_tambahButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // Save dataPenjualanValue to file .txt
        try {
            // Check dataPenjualanValue apakah valid atau tidak
            if (dataPenjualanValue.getText().equals("Data Customer Belum Lengkap!") || dataPenjualanValue.getText().equals("")) {
                infoText.setText("Data Penjualan Kosong!");
            } else {
                // get file name from saveText field
                String fileName = saveText.getText();
                // check if file name is empty
                if (fileName.equals("")) {
                    infoText.setText("Nama File Kosong!");
                } else if (!fileName.contains(".txt")) {
                    infoText.setText("Harus Memiliki Format .txt !");
                } else {
                    // create file with fileName
                    File file = new File(fileName);
                    // check if file already exists
                    if (file.exists()) {
                        infoText.setText("Nama File Sudah Ada!");
                    } else {
                        // create file
                        file.createNewFile();
                        // write dataPenjualanValue to file
                        FileWriter fileWriter = new FileWriter(fileName);
                        fileWriter.write(dataPenjualanValue.getText());
                        fileWriter.close();
                        infoText.setText("File Berhasil Disimpan!");
                    }
                }
            }
        } catch (IOException error) {
            infoText.setText("Terjadi Kesalahan");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void openButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openButtonActionPerformed
        // Open dataPenjualanValue from file .txt
        try {
            // get file name from openText field
            String fileName = openText.getText();
            // check if file name is empty
            if (fileName.equals("")) {
                infoText.setText("Nama File Kosong!");
            } else if (!fileName.contains(".txt")) {
                infoText.setText("Harus Memiliki Format .txt !");
            } else {
                // create file with fileName
                File file = new File(fileName);
                // check if file already exists
                if (!file.exists()) {
                    infoText.setText("Nama File Tidak Ada!");
                } else {
                    // read dataPenjualanValue from file
                    FileReader fileReader = new FileReader(fileName);
                    BufferedReader bufferedReader = new BufferedReader(fileReader);
                    String line;
                    String data = "";
                    while ((line = bufferedReader.readLine()) != null) {
                        data += line + "\n";
                    }
                    dataPenjualanValue.setText(data);
                    infoText.setText("File Berhasil Dibuka!");
                }
            }
        } catch (IOException error) {
            infoText.setText("Terjadi Kesalahan");
        }
    }//GEN-LAST:event_openButtonActionPerformed

